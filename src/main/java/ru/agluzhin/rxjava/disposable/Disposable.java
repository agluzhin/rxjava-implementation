package ru.agluzhin.rxjava.disposable;

/**
 * Интерфейс управления подпиской.
 *
 * <p>Позволяет потребителю отменить подписку на {@code Observable},
 * прекратив получение новых элементов и освободив ресурсы.</p>
 */
public interface Disposable {

    /**
     * Отменяет подписку. После вызова наблюдатель не будет получать новые элементы.
     */
    void dispose();

    /**
     * Возвращает {@code true}, если подписка уже отменена.
     *
     * @return состояние подписки
     */
    boolean isDisposed();
}
