package ru.agluzhin.rxjava.schedulers;

/**
 * Интерфейс планировщика выполнения задач.
 *
 * <p>Scheduler определяет, в каком потоке или пуле потоков будет
 * выполнена переданная задача. Это центральная абстракция для управления
 * многопоточностью в реактивных потоках.</p>
 */
public interface Scheduler {

    /**
     * Выполняет переданную задачу согласно стратегии данного планировщика.
     *
     * @param task задача для выполнения
     */
    void execute(Runnable task);

    /**
     * Освобождает ресурсы планировщика (пул потоков).
     * Должен вызываться при завершении работы приложения.
     */
    void shutdown();
}
