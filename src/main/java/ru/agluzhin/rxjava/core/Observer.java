package ru.agluzhin.rxjava.core;

/**
 * Интерфейс наблюдателя (Observer) — получает события из потока данных.
 *
 * <p>Соответствует паттерну «Наблюдатель»: Observer подписывается на {@link Observable}
 * и реагирует на три типа событий: получение элемента, ошибка и завершение потока.</p>
 *
 * @param <T> тип элементов потока
 */
public interface Observer<T> {

    /**
     * Вызывается при получении очередного элемента из потока.
     *
     * @param item полученный элемент
     */
    void onNext(T item);

    /**
     * Вызывается при возникновении ошибки в потоке.
     * После вызова этого метода поток считается завершённым с ошибкой.
     *
     * @param t возникшее исключение
     */
    void onError(Throwable t);

    /**
     * Вызывается при успешном завершении потока.
     * После вызова этого метода новые элементы не поступают.
     */
    void onComplete();
}
