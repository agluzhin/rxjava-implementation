package ru.agluzhin.rxjava.core;

/**
 * Интерфейс эмиттера — связующее звено между источником данных и наблюдателем.
 *
 * <p>Источник данных использует {@code Emitter} для отправки элементов,
 * ошибок и сигнала завершения. Эмиттер отвечает за безопасную передачу
 * событий и прекращение работы при отмене подписки.</p>
 *
 * @param <T> тип элементов потока
 */
public interface Emitter<T> {

    /**
     * Отправляет элемент в поток.
     *
     * @param item элемент для отправки
     */
    void onNext(T item);

    /**
     * Сигнализирует об ошибке и завершает поток.
     *
     * @param t возникшее исключение
     */
    void onError(Throwable t);

    /**
     * Сигнализирует об успешном завершении потока.
     */
    void onComplete();

    /**
     * Возвращает {@code true}, если подписка отменена и отправка элементов
     * не имеет смысла.
     *
     * @return состояние отмены
     */
    boolean isDisposed();
}
